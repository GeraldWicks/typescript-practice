function printNewPoint(point) {
    console.log("(x, y) = (".concat(point.x, ", ").concat(point.y, ")"));
}
printNewPoint({ x: 2, y: 4 });
function printThreeAxix(point) {
    console.log("(x, y, z) = (".concat(point.x, ", ").concat(point.y, ", ").concat(point.z, ")"));
}
printThreeAxix({ x: 3, y: 6, z: 12 });
